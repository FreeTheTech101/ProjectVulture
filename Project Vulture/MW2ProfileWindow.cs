        private void GiveGold(int ClientID, string GoldID)
        {
            int Client = ClientID;
            long Client0Offset = GameAddresses.GoldD;
            uint AllClientBaby = Convert.ToUInt32(Client0Offset + (GameAddresses.PlayerState1 * Client));
            Memorys mem = new Memorys("iw4mp");
